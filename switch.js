// Sentencias Switch

let nota = 5;

switch (nota){
    case 5:
        console.log("sobresaliente")
        break;
    case 4:
        console.log("Buen trabajo")
        break;
    case 3:
        console.log("suficiente")
        break;
    case 2:
        console.log("Aprobado")
        break;
    case 1:
        console.log("Insuficiente")
        break;
    default:
        console.log("Error");
}